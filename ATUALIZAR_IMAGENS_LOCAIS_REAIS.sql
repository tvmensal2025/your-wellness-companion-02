-- Verificar se todas as atualizações foram aplicadas
SELECT 
    name,
    image_url,
    CASE 
        WHEN image_url LIKE '/images/produtos/%' THEN '✅ Atualizado'
        ELSE '❌ Não atualizado'
    END as status
FROM public.supplements 
WHERE image_url IS NOT NULL
ORDER BY name;
